## LIST datatype examplew
''' In list, data items can be stored as string, array (which in python
 is reprsented as lists)..  Lists are versatile and can store item of diff datatypes.. '''
 
 
## syntax = items stored in 'SQUARE Brackets'  []


## List of string 
a = ["Krishna",'Shouryaman',"Tanmay"]
print(a)
print(type(a))

print("\n")

## List of List (array)
a = [ [1,2,3], [5,6,7], [9,0,4]]
print(a)
print(type(a))

print("\n")

## List of mixed data types
a = [ 1,2,3,45,66,99]
print(a)
print(type(a))