## Tuple datatype

''' In Tuple, data or items are stored similarly like of list..
but the item stored are immutable...  and Can store value of different data type  '''

### synatx = items in tuple are stored in "Parenthesis" ()  


# Tuple of Heterogenous datatype 
a = (20 ,"Shouryaman", 2004, "Jammu", 180001)
print(a)
print(type(a))

print("\n")

## Tuple of string datatype
a = ("Krishna", "Arjun", "balram")
print(a)
print(type(a))

print("\n")

## Tuple of mixed data types
a = (1,2,3,45,66,99)
print(a)
print(type(a))



