## LETS calculate the length of the string using 'len' function.

str_1 = "Lavish"
str_2 = "2004"
str_3 = " "

# Counting length of string
print("Length of str_1:",len(str_1))
print("Length of str_2:",len(str_2))
print("Length of str_3:",len(str_3))

# Calculating string length
final_string = len(str_1) + len(str_2) + len(str_3)
print("FInal string :",final_string)